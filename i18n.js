/**
 * Get the current active language (from localStorage or default 'nl')
 */
function getCurrentLang() {
  try {
    return localStorage.getItem('ic_lang') || 'nl';
  } catch (e) {
    return 'nl';
  }
}

/**
 * Translate a dot-path key for the current (or specified) language.
 * Falls back to Dutch if the key is missing.
 * @param {string} key  — e.g. 'nav.verkoop' or 'common.kopen'
 * @param {string} [lang]
 */
function t(key, lang) {
  lang = lang || getCurrentLang();
  var parts = key.split('.');
  var val = I18N[lang];
  var nlVal = I18N['nl'];

  for (var i = 0; i < parts.length; i++) {
    if (val) val = val[parts[i]];
    if (nlVal) nlVal = nlVal[parts[i]];
  }
  return val || nlVal || key;
}

/**
 * Apply translations to all elements with data-i18n or data-i18n-attr attributes.
 * Also updates the <html lang="..."> attribute.
 * @param {string} [lang]  — language code ('nl','en','de','fr','pt')
 */
function applyLanguage(lang) {
  lang = lang || getCurrentLang();

  // Validate
  if (!I18N[lang]) lang = 'nl';

  // Persist
  try { localStorage.setItem('ic_lang', lang); } catch (e) {}

  // Update html lang attribute
  document.documentElement.lang = lang;

  // Update the language switcher active state
  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.classList.toggle('lang-active', btn.dataset.lang === lang);
  });

  // Translate textContent — data-i18n="key.path"
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    var val = t(key, lang);
    if (val && val !== key) el.textContent = val;
  });

  // Translate innerHTML (for elements with sub-HTML) — data-i18n-html="key.path"
  document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-html');
    var val = t(key, lang);
    if (val && val !== key) el.innerHTML = val;
  });

  // Translate placeholder — data-i18n-ph="key.path"
  document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-ph');
    var val = t(key, lang);
    if (val && val !== key) el.placeholder = val;
  });

  // Translate aria-label — data-i18n-aria="key.path"
  document.querySelectorAll('[data-i18n-aria]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-aria');
    var val = t(key, lang);
    if (val && val !== key) el.setAttribute('aria-label', val);
  });

  // Fire custom event so page-specific JS can react
  document.dispatchEvent(new CustomEvent('ic:langchange', { detail: { lang: lang } }));
}

/**
 * Switch language, save to storage, re-apply.
 * @param {string} lang
 */
function switchLanguage(lang) {
  applyLanguage(lang);
}

// Auto-apply on DOM ready (runs before or after DOMContentLoaded depending on load order)
(function() {
  function doApply() {
    applyLanguage(getCurrentLang());
  }
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', doApply);
  } else {
    doApply();
  }
})();
